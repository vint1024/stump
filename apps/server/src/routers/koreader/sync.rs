#[derive(Debug, Serialize, Deserialize)]
struct KOReaderURLParams<D> {
	#[serde(flatten)]
	params: D,
	#[serde(default)]
	api_key: Option<String>,
}

#[derive(Debug, Serialize, Deserialize)]
struct KOReaderDocumentURLParams {
	document: String,
}

